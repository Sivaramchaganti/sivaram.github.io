// Smooth scrolling for navigation links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

// Mobile menu toggle
const menuToggle = document.querySelector('.menu-toggle');
const navMenu = document.querySelector('.nav-menu');

if (menuToggle) {
    menuToggle.addEventListener('click', () => {
        navMenu.classList.toggle('active');
        menuToggle.classList.toggle('active');
    });
}

// Close menu when clicking on a link
document.querySelectorAll('.nav-link').forEach(link => {
    link.addEventListener('click', () => {
        navMenu.classList.remove('active');
        menuToggle.classList.remove('active');
    });
});

// Typing animation
const typingEffectElement = document.querySelector('.typing-effect');
const textToType = "A passionate web developer";
let i = 0;

function typeWriter() {
    if (typingEffectElement && i < textToType.length) {
        typingEffectElement.textContent = textToType.substring(0, i + 1);
        i++;
        setTimeout(typeWriter, 100); // Adjust typing speed here
    }
}

if (typingEffectElement) {
    typingEffectElement.textContent = ''; // Clear text initially
    typeWriter();
}

// Navbar background on scroll
